#include <iostream>

using namespace std;

int main()
{
	auto a = 3;
	auto b = 'd';
	auto c = 3.49348;
	int d;
	char z;
	double k;
	
	cout << "the size of auto type a is " << sizeof(a);
	cout << endl;
	
	cout << "the size of auto type b is " << sizeof(b);
	cout << endl;
	
	cout << "the size of auto type c is " << sizeof(c);
	cout << endl;
	
	cout << "the size of int type d is " << sizeof(d);
	cout << endl;
	
	cout << "the size of char type z is " << sizeof(z);
	cout << endl;
	
	cout << "the size of double type k is " << sizeof(k);
	cout << endl;
}